#include "Span.hpp"

int main()
{
    Span sp = Span(5);
    sp.addNumber(6);
    sp.addNumber(3);
    sp.addNumber(17);
    sp.addNumber(9);
    sp.addNumber(11);
    std::cout << sp.shortestSpan() << std::endl;
    std::cout << sp.longestSpan() << std::endl;

    Span tab = Span(10000);
    tab.fill();
    std::cout << tab.shortestSpan() << std::endl;
    std::cout << tab.longestSpan() << std::endl;

    return 0;
}